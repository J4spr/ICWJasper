def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move():
    """Move pacman and all ghosts."""
    writer.undo()
    writer.write(state['score'])

    clear()
    global wishfulTimer

    if wishfulTimer > 0:
        wishfulTimer -= 1
        if valid(pacman + wishfulVector):
            aim.x = wishfulVector.x
            aim.y = wishfulVector.y
            wishfulTimer = 0

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for point, course in ghosts:
        options = [
            vector(5, 0),  # right
            vector(-5, 0),  # left
            vector(0, 5),  # up
            vector(0, -5)]  # down

        options.remove(vector(-course.x, -course.y))

        valid_options = []
        for option in options:
            next_position = point + option
            if valid(next_position):
                valid_options.append(option)

        if valid_options:
            current_direction = course.copy()
            if current_direction in valid_options and random.random() > 0.50:
                direction = current_direction
            else:
                direction = choice(valid_options)
                course.x, course.y = direction.x, direction.y

            if current_direction in valid_options and random.random() < 0.03:
                course.x, course.y = -direction.x, -direction.y

            point.move(direction)

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    for point, course in ghosts:
        if abs(pacman - point) < 20:
            return

    ontimer(move, 30)


def change(x, y):
    global wishfulTimer
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y
    else:
        wishfulVector.x = x
        wishfulVector.y = y
        wishfulTimer = 3


